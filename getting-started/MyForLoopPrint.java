public class ForLoopPrint {

  public static void main (String[] argv)
  {
	System.out.println (1); 

	for (int i=0; i<2; i++) {
	    System.out.print (2); 
	}
	System.out.println (); 

	for (int i=0; i<3; i++) {
	    System.out.print (3); 
	}
	System.out.println (); 

	for (int i=0; i<4; i++) { 
	    System.out.print (4); 
	}
	System.out.println (); 
	
	for (int i=0; i<5; i++) {
	    System.out.print (5); 
	}
	System.out.println (); 
   }

}
